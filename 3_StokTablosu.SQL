CREATE TABLE STOK (
    urun_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    urun_adi VARCHAR2(255) NOT NULL,
    miktar NUMBER(10, 2) NOT NULL,
    olcu_birimi VARCHAR2(10) NOT NULL
);

