CREATE TABLE SIPARISLER (
    siparis_numarasi NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    urun_adi VARCHAR2(255) NOT NULL,
    urun_ozellikleri VARCHAR2(500),
    olcu_birimi VARCHAR2(10) CHECK (olcu_birimi IN ('kg', 'adet', 'ton', 'gr')),
    miktar NUMBER(10, 2) NOT NULL,
    fiyati NUMBER(10, 2) NOT NULL,
    siparis_tarihi DATE DEFAULT SYSDATE,
    toplam_tutar AS (miktar * fiyati)
);

